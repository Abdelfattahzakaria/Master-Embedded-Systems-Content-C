typedef union{
	unsigned char x;
	short unsigned int y;
	float z;
}UNumberDT;

enum ENumType {FLOAT , SHORT_UNSIGNED_NT , CHAR};

//*********************************************************************************************
//*********************************************************************************************
UNumberDT Sum(UNumberDT first_number , UNumberDT second_number , enum ENumType type){
	UNumberDT result;
	switch(type){
	case SHORT_UNSIGNED_NT:{
		result.y= first_number.y + second_number.y;
		break;
	}
	case FLOAT: {
		result.z= first_number.z + second_number.z;
		break;
	}
	default: printf("Invalid passed type\n");
	}
	return result;
}


