/**********************************************************************************

                                     Conditional Operators

 ***********************************************************************************/

#include <stdio.h>

int main(void){
	unsigned char a= 70 , b= 50;
	/**********************************************************************************

                                       Relational Operators

	 **********************************************************************************/
	printf("a > b ?  %d\n" , a > b);
	printf("a >= b ?  %d\n" , a > b);
	printf("a < b ?  %d\n" , a > b);
	printf("a <= b ?  %d\n" , a > b);
	printf("a == b ?  %d\n" , a > b);
	printf("a != b ?  %d\n" , a > b);

	/**********************************************************************************

	                                       Relational Operators

	 **********************************************************************************/
	printf("(a >= b) && (a > 0) ?  %d\n" , (a >= b) && (a > 0));
	printf("(a <= b) || (a > 0) ?  %d\n" , (a <= b) || (a > 0));
	printf("!((a >= b) && (a > 0)) ?  %d\n" , !((a >= b) && (a > 0)));

	return 0;
}
